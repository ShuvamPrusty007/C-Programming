#include <stdio.h>

int main()
{
    printf("Arithmetic Opeations on int data type");

    int x = 5;
    int y = 2;
    printf("\n%d", x + y);
    printf("\n%d", x - y);
    printf("\n%d", x * y);
    printf("\n%d", x / y);

    printf("\n\nArithmetic Opeations on float data type:");

    float a = 5;
    float b = 2;
    printf("\n%f", a + b);
    printf("\n%f", a - b);
    printf("\n%f", a * b);
    printf("\n%f", a / b);

    return 0;
}